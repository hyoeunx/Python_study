# for 활용

#리스트
list = [1,2,3]
for i in list:
    print(i)
    
print('\n')
    
#튜플
tuple = (1,2,3)
for i in tuple:
    print(i)
    
print('\n')

#딕셔너리
hyoeun = {'이름':'심효은', '나이':'17'}
for i in hyoeun.values():
    print(i)
for i in hyoeun.keys():
    print(i) 
for k, v in hyoeun.items():
    print(k, v)
    
print('\n')    

#문자열
name = '심효은'
for a in name:
    print(a)