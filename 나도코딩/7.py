# 연산자

# 산술 연산자
print(5+2)
print(5-2)
print(5*2)
print(5/2)
print(5%2)
print(5//2) #몫
print(5**2) #거듭제곱

# 비교 연산자
print(5>2)  #True
print(5>=2) #True
print(5<2)  #False
print(5<=2) #Flase
print(5==2) #False
print(5!=2) #True

# 논리 연산자
print(3<5 and 7<5)  #둘 다 참이면 True #False
print(3<5 or 7<5)   #하나라도 참이면 True #True
print(not 3<5)  #반전 #False

# 멤버 연산자
print('c' in 'cat') #포함 #True
print('c' not in 'cat') #미포함 #False